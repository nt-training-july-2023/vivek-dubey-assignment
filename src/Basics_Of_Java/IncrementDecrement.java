package Basics_Of_Java;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a = 5;
		// it will print 5 and then will increase a by 1
		System.out.println(a++);
		// it will first increase a by 1 and then print 7
		System.out.println(++a);
		// it will first decrease a by 1 and then print the value of a as 6
		System.out.println(--a);
		// it will first print the value of a as 6 and then decrease it by 1
		System.out.println(a--);
		

	}

}
