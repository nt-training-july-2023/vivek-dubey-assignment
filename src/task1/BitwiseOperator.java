package task1;

public class BitwiseOperator {

	public static void main(String[] args) {
		
		// bitwise OR
		//It returns bit by bit OR of input values
		int a = 5;
		int b = 7;
		System.out.println(5|7);
		
		// bitwise AND
		//It returns bit by bit AND of input values
		System.out.println(5&7);
		
		//Bitwise XOR
		// It returns bit by bit XOR of input values, 
		System.out.println(5^7);
		
		
		//left shift operator multiplies the original number by 2
		System.out.println(a << 2);
		
		//right shift operator divides the original number by 2
		System.out.println(b >> 1);
		
		
	}

}
